namespace bmm {

// thr_to_anf — построение полинома Жегалкина (ANF) для заданной пороговой
// функции.
//
// Вход:  Thr (веса w_1..w_n, порог theta).
// Выход: Anf.
//
// !!! Теоретическая заметка: для пороговых функций (в частности, для
// большинства симметричных пороговых функций вроде MAJ) степень ANF растёт
// с n, и известных компактных ЗАМКНУТЫХ формул для произвольных весов
// (w_1..w_n, theta) в общем случае нет — это математическое свойство самих
// пороговых функций (см. также предупреждение в anf/anf_to_thr.hpp про
// потенциально экспоненциальный размер ANF), а не пробел в подготовке
// скелета. Практический путь для умеренных n: thr_to_tt (thr/thr_to_tt.hpp,
// тривиальная точечная оценка линейной формы) + tt_to_anf
// (anf/tt_to_anf.hpp, преобразование Мёбиуса). Для симметричных частных
// случаев (все веса равны — MAJ/AND/OR) существуют известные компактные
// формулы через биномиальные коэффициенты (элементарные симметрические
// полиномы по модулю 2) — если ваша тестовая пороговая функция именно
// такая, может быть выгоднее реализовать этот частный случай отдельно, а
// на общий (thr_to_tt+tt_to_anf) переключаться как на fallback.
//
// Параллелизм: core/CONVENTIONS.md п.6 — при реализации через thr_to_tt+
// tt_to_anf параллелизм наследуется от них (OpenMP на первом шаге, по
// заметке-исключению в конце п.6 — на втором); при прямом построении ANF
// для симметричного частного случая — TBB, т.к. результат строится как
// граф/структура Anf.
//
// Тесты: test_thr.cpp, секция "thr_to_anf".
Result<Anf> thr_to_anf(const Thr& thr);

}  // namespace bmm
