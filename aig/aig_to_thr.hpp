// aig_to_thr — распознавание, что функция, заданная AIG, является пороговой
// (threshold), и построение представления Thr (веса + порог).
//
// Вход:  Aig (mockturtle::aig_network, один PO).
// Выход: Thr (веса w_1..w_n + theta, f(x) = [sum w_i*x_i >= theta]).
//
// !! ВНИМАНИЕ, теоретическая сложность: в общем случае распознавание "эта
// булева функция — пороговая" и восстановление весов — не тривиальная по
// стоимости задача (не каждая функция пороговая; проверка требует, по сути,
// решения задачи разрешимости линейной системы неравенств). Классический
// точный путь — Muroga, S., "Threshold Logic and Its Applications", Wiley,
// 1971: свести восстановление весов к LP/ILP-задаче на n+1 переменную
// (w_1..w_n, theta) с одним неравенством на каждую точку истинности функции
// (>= theta, если f(x)=1, < theta иначе), и решить её LP/ILP-солвером.
// Google OR-Tools (`ortools::ortools`, добавлен во второй итерации — см.
// core/CONVENTIONS.md п.5а, `bmm_aig` уже его линкует) — доступный в этом
// стеке ILP-солвер для этого шага (см. `ortools::sat::CpModelBuilder` или
// `ortools::glop` для чисто линейного случая — сверьтесь с актуальным API,
// он менялся между релизами). Chow-параметры (см.
// verify/metamorphic/metamorphic.hpp) дают
// быстрый НЕОБХОДИМЫЙ (но не достаточный) предварительный фильтр — их
// можно использовать, чтобы дёшево отбросить заведомо непороговые функции
// перед дорогим LP-шагом.
//
// Если по итогам анализа функция не является пороговой — верните
// ErrorCode::Unsupported (НЕ NotImplemented: это означает "для конкретно
// этого входа результата не существует", а не "функция ещё не написана").
//
// Параллелизм: core/CONVENTIONS.md п.6, правило 2 — TBB для фазы обхода AIG
// (например, при построении Chow-параметров/симуляции по узлам с
// мемоизацией). Сам LP/ILP-решатель типично последовательный в большинстве
// доступных реализаций — не пытайтесь его параллелить искусственно.
//
// Обязательный бенчмарк (вторая итерация задания): TBB используется для
// фазы обхода AIG (см. "Параллелизм" выше) — реализация ДОЛЖНА пройти
// секцию "aig_to_thr_tbb_scaling" в test_aig.cpp (замер 1-поточного vs
// полного tbb::global_control на n=8/12/kMaxReferenceAigVars, см.
// benchmarks/tbb_scaling.hpp). Если на этих размерах LP/ILP-шаг доминирует
// над временем обхода AIG (вполне вероятно — сам солвер не распараллелен
// вручную, см. выше) — заметный speedup может не появиться, это валидный
// зафиксированный результат, а не повод считать бенчмарк проваленным.
//
// Тесты: test_aig.cpp, секции "aig_to_thr" (корректность) и
// "aig_to_thr_tbb_scaling" (обязательный бенчмарк параллельности).
Result<Thr> aig_to_thr(const Aig& aig);

