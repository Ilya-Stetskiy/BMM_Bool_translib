// bdd_to_thr — распознавание, что функция, заданная BDD, является пороговой
// (threshold), и построение представления Thr.
//
// Вход:  Bdd (sylvan::Bdd).
// Выход: Thr (веса + порог).
//
// Общего полиномиального алгоритма для этого направления НЕ существует (в
// отличие от противоположного thr_to_bdd, см. thr/thr_to_bdd.hpp — Behle
// 2007/2008, Hosaka 1997 — те про построение BDD ИЗ уже известной пороговой
// функции, а не про распознавание пороговости произвольного BDD).
// Во второй итерации задания это уточнено до конкретного пайплайна вместо
// общей отсылки к "bdd_to_tt + tt_to_thr" — реализуйте МНОГОСТУПЕНЧАТЫЙ
// ФИЛЬТР-ПАЙПЛАЙН:
//
//  1. Проверка унитонности (unateness) по кофакторам: пороговые функции
//     ВСЕГДА унитонны по каждой переменной (классический факт теории
//     пороговой логики) — необходимое условие, дешёвое отбросить заведомо
//     непороговые функции. Для каждой переменной x_i проверьте f|x_i=0 =>
//     f|x_i=1 (положительная унитонность) ИЛИ f|x_i=1 => f|x_i=0
//     (отрицательная) — через импликацию BDD-кофакторов: `(cofactor0 &
//     cofactor1) == cofactor0` даёт "cofactor0 влечёт cofactor1" (тот же
//     приём, что и в `bmm::Bdd::evaluate()` в core/common.hpp для
//     "cube влечёт root"). Сложность O(|V|) проверок, каждая — О(размера
//     BDD) в худшем случае за счёт кэша операций Sylvan. Не унитонна хотя
//     бы по одной переменной -> сразу возвращайте ErrorCode::Unsupported,
//     дальше не считайте.
//  2. Вычисление вектора Чоу (Chow parameters, см.
//     verify/metamorphic/metamorphic.hpp::compute_chow_parameters — ТА
//     реализация не для production-кода, она для тестов; здесь нужен
//     BOTTOM-UP обход по СТРУКТУРЕ BDD, а не полный перебор точек) —
//     считает вес функции (число точек ON-набора) и корреляцию с каждой
//     переменной, аккумулируя число путей от каждого узла до терминала-1 с
//     учётом кратности "пропущенных" на BDD-путях переменных (BDD может не
//     ветвиться по переменной, если под-функция от неё не зависит на этом
//     пути — тогда путь считается за 2^(число пропущенных переменных)
//     присвоений, стандартная тонкость BDD-based подсчёта, легко упустить).
//  3. Для n<=8: вектор Чоу уникально определяет функцию среди пороговых
//     функций этой размерности (классический результат в справочных
//     таблицах теории пороговой логики, восходит к перечислениям Мурога и
//     последователей) — заведите/найдите справочную таблицу "вектор Чоу ->
//     веса" для n<=8 и берите веса напрямую, без ILP. Для n>8: извлеките из
//     вектора Чоу ограничения — симметрия (CH_i == CH_j => w_i == w_j) и
//     порядок весов (CH_i > CH_j => w_i >= w_j) — это не полное решение, а
//     СУЖЕНИЕ пространства поиска для шага 4.
//  4. ILP-солвер (Google OR-Tools, `ortools::ortools`, см.
//     core/CONVENTIONS.md п.5а) на PRIME IMPLICANTS, извлечённых из
//     структуры BDD (не на полной таблице истинности — простых
//     импликант(ов) как правило на порядки меньше, чем 2^n точек; для
//     извлечения prime implicants из BDD/ZDD-структуры см. Minato, S.,
//     "Zero-Suppressed BDDs for Set Manipulation in Combinatorial
//     Problems", 1993 — ZDD-based генерация множеств простых импликант).
//     Каждая простая импликанта ON-набора даёт ограничение вида "сумма
//     весов по литералам импликанты >= theta" (это самая tight-констрейнт
//     точка, покрываемая импликантой — сверьтесь с литературой по exact
//     threshold synthesis через unate covering за точной формулировкой,
//     нюансы возможны при отрицательных весах/отрицательно-унитонных
//     переменных), аналогично для простых импликант OFF-набора — "< theta".
//     Плюс ограничения симметрии/порядка из шага 3. **INFEASIBLE на выходе
//     солвера — ВАЛИДНЫЙ результат** ("псевдопороговая функция" — унитонна,
//     но не пороговая), это не ошибка солвера и не баг — в этом случае
//     верните ErrorCode::Unsupported.
//
// Если по итогам анализа (на любом шаге 1 или 4) функция не является
// пороговой — верните ErrorCode::Unsupported (не NotImplemented).
//
// Обязательный try/catch(std::bad_alloc&) НЕ требуется для этой функции
// специально (в отличие от bdd_to_aig) — размер промежуточных структур
// здесь ограничен |BDD| и числом простых импликант, не растёт
// неконтролируемо тем же способом; если на практике обнаружится обратное —
// дополните CONVENTIONS.md п.2а.
//
// Параллелизм: core/CONVENTIONS.md п.6, правило 1 — Bdd на входе, поэтому
// обход/чтение структуры BDD (шаги 1–3) — только через Sylvan API. Сам вызов
// OR-Tools (шаг 4) можно делать после того, как обход BDD завершён — это
// уже не работа с BDD-узлами, ограничение "только Sylvan/Lace" на сам
// ILP-солвер не распространяется.
//
// Тесты: test_bdd.cpp, секция "bdd_to_thr".
Result<Thr> bdd_to_thr(const Bdd& bdd);

