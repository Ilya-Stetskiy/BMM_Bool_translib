#include "core/anf_repr.hpp"

namespace bmm {

// bdd_to_anf — построение полинома Жегалкина (ANF) из BDD.
//
// Вход:  Bdd (sylvan::Bdd).
// Выход: Anf (BoolePolynomial при BMM_HAVE_BRIAL, иначе AnfFallback — см.
//        core/CONVENTIONS.md п.5).
//
// Алгоритм/литература (уточнено во второй итерации задания): ГИБРИДНАЯ
// стратегия — основной путь + fallback по порогу.
//
// Основной путь — прямой BOTTOM-UP обход по BDD (в отличие от bdd_to_tt,
// где top-down/диапазонный обход обязателен — здесь мемоизация по узлу
// РАБОТАЕТ корректно, т.к. локальный ANF узла — компактная структура
// (разреженный набор мономов), не зависящая от позиции/контекста, в
// отличие от "локального куска таблицы истинности" в bdd_to_tt), с заменой
// разложения Шеннона на ПОЛОЖИТЕЛЬНОЕ РАЗЛОЖЕНИЕ ДЭВИО:
//     ANF(node) = ANF(low) ⊕ x · (ANF(low) ⊕ ANF(high))
// (эквивалентно записи ANF(lo) XOR x_i*(ANF(hi) XOR ANF(lo)) — та же формула,
// связывающая кофакторы функции с её полиномом Жегалкина через разложение
// Дэвио, см. Carlet, "Boolean Functions for Cryptography...", 2010, раздел
// про построение ANF из разложения по переменной). Хранить ANF узла как
// РАЗРЕЖЕННЫЙ набор мономов (см. `AnfFallback`/`Monomial` в
// core/common.hpp, либо `BoolePolynomial` при BMM_HAVE_BRIAL); XOR двух
// наборов — merge/symmetric difference (см.
// `AnfFallback::add_monomial` — XOR-сокращение при повторной вставке).
// Обход узлов BDD — через Sylvan API, мемоизация BDD-узел -> уже
// построенный частичный Anf-полином обязательна (иначе экспоненциальный
// рост при разделяемых поддеревьях BDD).
//
// Fallback: если промежуточные разреженные наборы мономов на каком-то узле
// растут сверх настраиваемого порога (подберите константу эмпирически,
// например через benchmarks/ — жёстко правильного значения нет, зависит от
// формы конкретных входов) — переключайтесь на путь через полную
// материализацию: TruthTable (см. bdd_to_tt.hpp за тем, как это сделать
// эффективно) + Fast Möbius Transform, O(n·2^n) (тот же алгоритм, что и в
// anf/tt_to_anf.hpp — независимая эталонная реализация есть в
// verify/metamorphic/metamorphic.cpp::mobius_transform для примера, НЕ
// вызывайте её из production-кода — она часть тестовой инфраструктуры;
// если в вашей версии kitty есть готовая функция для этого преобразования
// — сверьтесь с актуальными заголовками kitty, схема не гарантирует
// конкретное имя API). Это gracefully деградирует к худшему, но
// гарантированному по асимптотике варианту вместо неограниченного роста
// разреженного представления.
//
// Важно: BDD и FDD/ANF ДОКАЗУЕМО НЕСРАВНИМЫ по размеру в обе стороны
// (Becker, B., Drechsler, R., Werchner, R., "On the relation between BDDs
// and FDDs", 1995 — есть функции, у которых BDD экспоненциально меньше
// ANF/FDD-представления, и наоборот) — если после реализации вы видите
// случаи взрывного роста в одну или другую сторону, это не обязательно
// баг, это может быть математическое свойство конкретной тестовой функции.
//
// Параллелизм: core/CONVENTIONS.md п.6, правило 1 — Bdd является входом,
// поэтому обход только через Sylvan API, без собственного TBB/OpenMP.
// Сборка результирующего Anf (умножение на x_i, XOR полиномов) может быть
// внутренне тяжёлой по памяти — если используете AnfFallback
// (std::set<Monomial>), учитывайте, что XOR двух больших множеств мономов
// — операция, которую стоит делать последовательно после того, как обход
// BDD завершён, а не параллельно с ним.
//
// Внимание при обходе BDD: физический уровень Sylvan узла (bdd.raw().TopVar())
// не обязан совпадать с индексом переменной — см. Bdd::var_at_level() в
// core/common.hpp и обоснование в anf/anf_to_bdd.cpp ("Часть 2"). Единственный
// сейчас производитель Bdd с нестандартным порядком уровней — anf_to_bdd;
// переменная монома, добавляемого в ANF на данном узле, — это
// bdd.var_at_level(node.TopVar()), не сам TopVar() напрямую.
//
// Тесты: test_bdd.cpp, секция "bdd_to_anf".
Result<Anf> bdd_to_anf(const Bdd& bdd);

}  // namespace bmm
