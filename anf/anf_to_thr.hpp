namespace bmm {

// anf_to_thr — распознавание, что функция, заданная ANF, является пороговой
// (threshold), и построение представления Thr.
//
// Вход:  Anf.
// Выход: Thr.
//
// !!! Частично теоретическая заметка: как и в aig_to_thr/bdd_to_thr, общая
// задача "пороговая функция или нет, и какие веса" не зависит от того, в
// каком представлении дана функция — практический путь для умеренных n:
// anf_to_tt (см. anf/anf_to_tt.hpp) + tt_to_thr (Chow-параметры + LP/ILP
// через Google OR-Tools, Muroga 1971 — см. aig/aig_to_thr.hpp и
// core/CONVENTIONS.md п.5а за подробностями, `bmm_anf` уже линкует
// `ortools::ortools`). Прямого способа
// читать веса из СТРУКТУРЫ ANF (набора мономов) без материализации полной
// таблицы истинности для больших n в рамках подготовки этого скелета не
// найдено. Дополнительно стоит иметь в виду: степень ANF порогового
// монотонного самодвойственного класса функций в общем случае может расти
// быстро с n (это математическое свойство самих пороговых функций, а не
// пробел в методе) — то есть даже если распознавание в принципе решено,
// материализация ANF для больших n может быть дорогой по памяти сама по
// себе, независимо от выбранного алгоритма перевода.
//
// Если по итогам анализа функция не является пороговой — верните
// ErrorCode::Unsupported (не NotImplemented).
//
// Параллелизм: core/CONVENTIONS.md п.6, правило 2 — TBB для фазы обхода
// мономов ANF (если используется частичный анализ структуры, например для
// Chow-параметров без полной материализации TT).
//
// Обязательный бенчмарк (вторая итерация задания): TBB используется для
// фазы обхода мономов ANF (см. "Параллелизм" выше) — реализация ДОЛЖНА
// пройти секцию "anf_to_thr_tbb_scaling" в test_anf.cpp (замер 1-поточного
// vs полного tbb::global_control на n=8/12/16, см.
// benchmarks/tbb_scaling.hpp). Как и в aig_to_thr — если LP/ILP-шаг
// доминирует над обходом ANF, заметный speedup может не появиться, это
// валидный зафиксированный результат.
//
// Тесты: test_anf.cpp, секции "anf_to_thr" (корректность) и
// "anf_to_thr_tbb_scaling" (обязательный бенчмарк параллельности).
Result<Thr> anf_to_thr(const Anf& anf);

}  // namespace bmm
