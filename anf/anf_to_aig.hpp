// anf_to_aig — построение AIG из полинома Жегалкина (ANF).
//
// Вход:  Anf.
// Выход: Aig (mockturtle::aig_network, один PO).
//
// Алгоритм/литература (уточнено во второй итерации задания): XAG-based
// подход — ANF по определению это XOR суммы AND-мономов, что естественно
// строится как XOR-AND граф (XAG); поскольку итоговое представление именно
// AIG (только AND+инверсия, без отдельного XOR-гейта), пошагово:
//
//  1. BuildMonomial(моном): каждый моном (произведение переменных монома) —
//     AND-цепочка по его переменным СО STRUCTURAL HASHING (не создавать
//     новый AND-узел, если такой AND(a,b) уже есть в графе — переиспользовать
//     существующий сигнал; mockturtle::aig_network::create_and сама
//     структурно хеширует по умолчанию, если через неё строить). Для
//     длинных мономов (много переменных) — не последовательная цепочка
//     AND(AND(AND(x1,x2),x3),x4)... (глубина O(t) для монома из t
//     переменных), а СБАЛАНСИРОВАННОЕ дерево AND — глубина O(log t).
//  2. XOR_AIG(a,b) — трёхвентильная стандартная AIG-кодировка XOR:
//     t1 = AND(a, ¬b), t2 = AND(¬a, b), XOR_AIG(a,b) = ¬AND(¬t1, ¬t2)
//     (т.е. NOT(AND(NOT t1, NOT t2)) — по де Моргану это OR(t1,t2), а
//     OR(t1,t2) истинен ровно когда истинно ровно одно из a,b — определение
//     XOR). Ровно 3 AND-гейта на один XOR, минимальная стандартная
//     AIG-кодировка.
//  3. Объединение мономов через XOR_AIG — НЕ последовательной цепочкой
//     (глубина O(k) для k мономов), а деревом параллельной редукции
//     (turnament-style попарное объединение) — глубина O(log k). Это не
//     только про параллелизм (см. ниже) — это ещё и меньшая глубина AIG
//     на выходе независимо от того, гоняете вы это в один поток или нет.
//  4. В конце — FRAIG-оптимизация (functionally reduced AIG: слияние
//     функционально эквивалентных узлов через SAT-sweeping/симуляцию,
//     стандартный постпроцессинг после структурного построения) — в
//     mockturtle есть готовые алгоритмы в этом духе (см.
//     mockturtle/algorithms, например *fraig*/*sat_sweeping*-семейство —
//     сверьтесь с актуальным API вашей версии mockturtle, конкретное имя
//     функции могло измениться между версиями).
//
// Вход:  Anf.
// Выход: Aig (mockturtle::aig_network, один PO).
//
// Параллелизм: core/CONVENTIONS.md п.6, правило 2 — TBB. И BuildMonomial
// (независимые деревья AND для разных мономов), и объединение мономов
// (дерево параллельной редукции из шага 3 — соседние пары независимы,
// естественная task_group-рекурсия) хорошо параллелятся; concurrent_hash_map
// — для мемоизации повторяющихся под-произведений переменных между разными
// мономами (если в ANF много мономов с общими префиксами переменных).
//
// Обязательный бенчмарк (вторая итерация задания): реализация ДОЛЖНА пройти
// секцию "anf_to_aig_tbb_scaling" в test_anf.cpp (замер 1-поточного vs
// полного tbb::global_control на n=8/12/16, см. benchmarks/tbb_scaling.hpp).
//
// Тесты: test_anf.cpp, секции "anf_to_aig" (корректность) и
// "anf_to_aig_tbb_scaling" (обязательный бенчмарк параллельности).
Result<Aig> anf_to_aig(const Anf& anf);

